    private void jButton22ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton22ActionPerformed
        if (!txt_Visor.getText().isEmpty()){
        if (!operador.equals("")){
            try{
        num2 = Float.valueOf(txt_Visor.getText());
        switch (operador){
            case "+" -> txt_Visor.setText(Float.toString(num1+num2));
            case "-" -> txt_Visor.setText(Float.toString(num1-num2));
            case "x" -> txt_Visor.setText(Float.toString(num1*num2));
            case "/" -> {
                    if (num2 != 0) {
                         txt_Visor.setText(Float.toString(num1 / num2));
                    } else {
                      txt_Visor.setText("Syntax Error");
                           }
                        }
            case "^" -> txt_Visor.setText(Float.toString((float) Math.pow(num1, num2)));
            default -> {
                    } 
        }
        }catch(NumberFormatException e){
            txt_Visor.setText("Syntax Error");
        }
        }
        }
        
            
    }//GEN-LAST:event_jButton22ActionPerformed

